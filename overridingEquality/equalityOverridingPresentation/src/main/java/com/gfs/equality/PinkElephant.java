package com.gfs.equality;

public class PinkElephant {

	private final int height;
	private final int weight;
	private final int age;
	private final char sex;
	
	public PinkElephant(int height, int weight, int age, char sex) {
		this.height = height;
		this.weight = weight;
		this.age = age;
		this.sex = sex;
	}
	
	public int getHeight() {
		return height;
	}

	public int getWeight() {
		return weight;
	}

	public int getAge() {
		return age;
	}

	public int getSex() {
		return sex;
	}
	
}
